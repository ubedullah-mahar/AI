# This task demonstrates the use of variables, basic data types,
# arithmetic operations, and value updates in Python.

name = "Ubedullah"
age = 20
height = 1.7  # height in meters

print("Name:", name)
print("Age:", age)
print("Height (in meters):", height)

# Updating age after 5 years
age += 5
print("Age after 5 years:", age)

# Converting height from meters to centimeters
height_in_cm = height * 100
print("Height (in centimeters):", height_in_cm)

# Reassigning age to a new value
age = 25
print("Updated age:", age)
